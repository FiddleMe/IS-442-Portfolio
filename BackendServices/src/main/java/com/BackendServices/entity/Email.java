package com.BackendServices.entity;

public class Email {
    private String sender;
    private String recipient;
    private String subject;
    private String msg;
    
    public String getSender() {
        return sender;
    }

    public String getRecipient() {
        return recipient;
    }

    public String getSubject() {
        return subject;
    }

    public String getMsg() {
        return msg;
    }

}
